import {writeFile} from 'node:fs/promises';
import {fileURLToPath} from 'node:url';

const allowed = new Set(['development', 'staging', 'production']);
const selected = process.argv[2];

if (!allowed.has(selected)) {
  console.error(
    `Expected one of ${Array.from(allowed).join(', ')}, received ${selected ?? 'nothing'}.`,
  );
  process.exit(1);
}

const output = fileURLToPath(
  new URL('../src/config/generatedEnvironment.ts', import.meta.url),
);
const contents = `// Generated by scripts/select-environment.mjs. Do not edit manually.\nexport const BUILD_ENVIRONMENT = '${selected}' as const;\n`;

await writeFile(output, contents, 'utf8');
console.log(`Selected VisionCart ${selected} environment.`);
